#include<iostream>
using namespace std;
int main()
{
    int a=10;
    int b=20;
    double c=33.45;
    long d=1234;
    char e='f';
    cout<<"size of a="<<sizeof(a)<<endl;
    cout<<"size of b="<<sizeof(b)<<endl;
    cout<<"size of c="<<sizeof(c)<<endl;
    cout<<"size of d="<<sizeof(d)<<endl;
    cout<<"size of e="<<sizeof(e)<<endl;
    return 0;
}
